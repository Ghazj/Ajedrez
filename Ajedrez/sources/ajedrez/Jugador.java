package ajedrez;

public abstract class Jugador {

	private String nombre;
	private int number;
	private String color;
	private int puntos;
	private int tiempo;
	
	public Jugador(String nombre, int number, String color, int puntos, int tiempo) {
		super();
		this.nombre = nombre;
		this.number = number;
		this.color = color;
		this.puntos = puntos;
		this.tiempo = tiempo;
	}
	
	
	//cambio para probar GIT
	
}
